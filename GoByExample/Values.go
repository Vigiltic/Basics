package main

import "fmt"

func main() {
	//Add the strings together to print "golang".
	fmt.Println("go" + "lang")
	//Print "1+1 =", then add 1+1 and combine with the string to get "1+1 = 2".
	fmt.Println("1+1 =", 1+1)
	//Print "7.0/3.0 =", then divide 7.0/3.0 and combine with the string.
	fmt.Println("7.0/3.0 =", 7.0/3.0)
	//Print the boolean values to the console
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
}
